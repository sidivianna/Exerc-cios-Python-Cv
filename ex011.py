lar = float(input("Qual é a largura da parede: "))
alt = float(input("Qual é a altura da parede: "))
area = lar * alt
tinta = area / 2

print(f"Largura da parede é {lar}")
print(F"Altura da parede é {alt}")
print(F"Sua parede tem a dimensão de {lar} x {alt} e sua área é de {area}m².")
print(f"Para pintar essa parede, você precisará de {tinta} de tinta.")

